def P(**kw):
    defaults = {
        "aiImagePrompt": f"{kw.get('title','')} {kw.get('author','')} 古风水墨画，朦胧山水，云雾缭绕，诗意盎然",
        "aiImageUrl": "", "historicalPaintings": [], "calligraphyUrls": [],
        "quizIds": [], "schoolLevel": "", "schoolTextbook": "",
        "theme": [], "relatedPoemIds": [],
    }
    return {**defaults, **kw}

AUTHORS = []
POEMS = []

def A(id, name, nickname, dynasty, bio, birth=None, death=None):
    AUTHORS.append({"id": id, "name": name, "nickname": nickname, "dynasty": dynasty,
                    "birthYear": birth, "deathYear": death, "bio": bio,
                    "representativeWorks": [], "portraitUrl": ""})

def poem(id, title, author, author_id, dynasty, category, difficulty, content,
         translation, annotation="", background="", tags=None, source="",
         school_level="", school_textbook="", theme=None):
    POEMS.append(P(
        id=id, title=title, author=author, authorId=author_id,
        dynasty=dynasty, category=category, difficulty=difficulty,
        content=content, translation=translation, annotation=annotation,
        background=background, tags=tags or [], source=source,
        schoolLevel=school_level, schoolTextbook=school_textbook,
        theme=theme or []
    ))

